package org.test.java;

public class SwitchCase {
	public static void main(String[] args) {
		String i = "bye";
	switch(i) {
	case "hello":
		System.out.println("hello");
		break;
	case "hi":
		System.out.println("hi");
		break;
		default:
			System.out.println("invalid");
			break;
		
		
	}
	}
	

}
